package agendaTelefonica;

/**
 *
 * @author samuel
 */
public class Dado {
    private String dado;
    private int inicio;
    private int fim;

    public Dado() {
      
    }
    
    public String getDado() {
        return dado;
    }

    public void setDado(String dado) {
        this.dado = dado;
    }
    

    public int getInicio() {
        return inicio;
    }

    public void setInicio(int inicio) {
        this.inicio = inicio;
    }

    public int getFim() {
        return fim;
    }

    public void setFim(int fim) {
        this.fim = fim;
    }
    
}
